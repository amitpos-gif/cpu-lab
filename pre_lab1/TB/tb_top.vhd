library IEEE;
use ieee.std_logic_1164.all;
use ieee.std_logic_arith.all;
use ieee.std_logic_unsigned.all;
use work.aux_package.all;

entity tb_top is
end tb_top;

architecture rtb of tb_top is
    constant n : integer := 8;
    constant k : integer := 3;

    signal Y_i : std_logic_vector(n-1 downto 0);
    signal X_i : std_logic_vector(n-1 downto 0);
    signal ALUFN_i : std_logic_vector(4 downto 0);
    signal ALUout_o : std_logic_vector(n-1 downto 0);
    signal Nflag_o, Cflag_o, Zflag_o, Vflag_o : std_logic;

    component top
        generic (n : integer := 8;
                 k : integer := 3;
                 m : integer := 4);
        port (Y_i, X_i : in std_logic_vector(n-1 downto 0);
              ALUFN_i : in std_logic_vector(4 downto 0);
              ALUout_o : out std_logic_vector(n-1 downto 0);
              Nflag_o, Cflag_o, Zflag_o, Vflag_o : out std_logic);
    end component;

begin

    DUT : top
        generic map (n => n, k => k, m => 4)
        port map (
            Y_i => Y_i,
            X_i => X_i,
            ALUFN_i => ALUFN_i,
            ALUout_o => ALUout_o,
            Nflag_o => Nflag_o,
            Cflag_o => Cflag_o,
            Zflag_o => Zflag_o,
            Vflag_o => Vflag_o
        );

    tb : process
    begin
        ----------------------------------------------------------------
        -- ARITHMETIC OPERATIONS (ALUFN[4:3] = "01")
        ----------------------------------------------------------------

        ----------------------------------------------------------------
        -- ADD: ALUFN = 01000 (Y + X)
        ----------------------------------------------------------------
        ALUFN_i <= "01000";

        -- 0 + 0
        Y_i <= "00000000";
        X_i <= "00000000";
        wait for 50 ns;

        -- 5 + 3
        Y_i <= "00000101";
        X_i <= "00000011";
        wait for 50 ns;

        -- 127 + 1 (overflow: positive + positive = negative)
        Y_i <= "01111111";
        X_i <= "00000001";
        wait for 50 ns;

        -- 255 + 1 (carry out)
        Y_i <= "11111111";
        X_i <= "00000001";
        wait for 50 ns;

        ----------------------------------------------------------------
        -- SUB: ALUFN = 01001 (Y - X)
        ----------------------------------------------------------------
        ALUFN_i <= "01001";

        -- 5 - 3
        Y_i <= "00000101";
        X_i <= "00000011";
        wait for 50 ns;

        -- 3 - 5 (borrow)
        Y_i <= "00000011";
        X_i <= "00000101";
        wait for 50 ns;

        -- -128 - 1 (overflow: negative - positive = positive)
        Y_i <= "10000000";
        X_i <= "00000001";
        wait for 50 ns;

        -- 0 - 0
        Y_i <= "00000000";
        X_i <= "00000000";
        wait for 50 ns;

        ----------------------------------------------------------------
        -- NEG: ALUFN = 01010 (neg(X) = 2's complement of X)
        ----------------------------------------------------------------
        ALUFN_i <= "01010";

        -- neg(5)
        Y_i <= "00000000";
        X_i <= "00000101";
        wait for 50 ns;

        -- neg(0)
        Y_i <= "00000000";
        X_i <= "00000000";
        wait for 50 ns;

        -- neg(128)
        Y_i <= "00000000";
        X_i <= "10000000";
        wait for 50 ns;

        ----------------------------------------------------------------
        -- Y + 2: ALUFN = 01011
        ----------------------------------------------------------------
        ALUFN_i <= "01011";

        Y_i <= "00000101";
        X_i <= "11111111";  -- X is ignored
        wait for 50 ns;

        Y_i <= "11111111";
        X_i <= "00000000";
        wait for 50 ns;

        ----------------------------------------------------------------
        -- Y - 2: ALUFN = 01100
        ----------------------------------------------------------------
        ALUFN_i <= "01100";

        Y_i <= "00000101";
        X_i <= "00000000";  -- X is ignored
        wait for 50 ns;

        Y_i <= "00000010";
        X_i <= "11111111";
        wait for 50 ns;

        ----------------------------------------------------------------
        -- SHIFT OPERATIONS (ALUFN[4:3] = "10")
        ----------------------------------------------------------------

        ----------------------------------------------------------------
        -- LEFT SHIFT: ALUFN = 10000 (SHL Y, X[k-1:0])
        ----------------------------------------------------------------
        ALUFN_i <= "10000";

        -- SHL by 0
        Y_i <= "00000001";
        X_i <= "00000000";
        wait for 50 ns;

        -- SHL by 1
        Y_i <= "00000001";
        X_i <= "00000001";
        wait for 50 ns;

        -- SHL by 2
        Y_i <= "00000001";
        X_i <= "00000010";
        wait for 50 ns;

        -- SHL by 3 (with carry out)
        Y_i <= "10000000";
        X_i <= "00000001";
        wait for 50 ns;

        -- SHL with pattern
        Y_i <= "10101010";
        X_i <= "00000001";
        wait for 50 ns;

        ----------------------------------------------------------------
        -- RIGHT SHIFT: ALUFN = 10001 (SHR Y, X[k-1:0])
        ----------------------------------------------------------------
        ALUFN_i <= "10001";

        -- SHR by 0
        Y_i <= "10000000";
        X_i <= "00000000";
        wait for 50 ns;

        -- SHR by 1
        Y_i <= "10000000";
        X_i <= "00000001";
        wait for 50 ns;

        -- SHR by 2
        Y_i <= "10000000";
        X_i <= "00000010";
        wait for 50 ns;

        -- SHR by 3 (with carry out)
        Y_i <= "00000001";
        X_i <= "00000001";
        wait for 50 ns;

        ----------------------------------------------------------------
        -- LOGIC OPERATIONS (ALUFN[4:3] = "11")
        ----------------------------------------------------------------

        ----------------------------------------------------------------
        -- NOT(Y): ALUFN = 11000
        ----------------------------------------------------------------
        ALUFN_i <= "11000";

        Y_i <= "00000000";
        X_i <= "11111111";  -- X is ignored
        wait for 50 ns;

        Y_i <= "11111111";
        X_i <= "00000000";
        wait for 50 ns;

        Y_i <= "10101010";
        X_i <= "01010101";
        wait for 50 ns;

        ----------------------------------------------------------------
        -- Y OR X: ALUFN = 11001
        ----------------------------------------------------------------
        ALUFN_i <= "11001";

        Y_i <= "00001111";
        X_i <= "11110000";
        wait for 50 ns;

        Y_i <= "10101010";
        X_i <= "01010101";
        wait for 50 ns;

        Y_i <= "00000000";
        X_i <= "00000000";
        wait for 50 ns;

        ----------------------------------------------------------------
        -- Y AND X: ALUFN = 11010
        ----------------------------------------------------------------
        ALUFN_i <= "11010";

        Y_i <= "00001111";
        X_i <= "11110000";
        wait for 50 ns;

        Y_i <= "10101010";
        X_i <= "10101010";
        wait for 50 ns;

        Y_i <= "11111111";
        X_i <= "00001111";
        wait for 50 ns;

        ----------------------------------------------------------------
        -- Y XOR X: ALUFN = 11011
        ----------------------------------------------------------------
        ALUFN_i <= "11011";

        Y_i <= "00001111";
        X_i <= "11110000";
        wait for 50 ns;

        Y_i <= "10101010";
        X_i <= "01010101";
        wait for 50 ns;

        Y_i <= "11111111";
        X_i <= "11111111";
        wait for 50 ns;

        ----------------------------------------------------------------
        -- Y NOR X: ALUFN = 11100
        ----------------------------------------------------------------
        ALUFN_i <= "11100";

        Y_i <= "00000000";
        X_i <= "00000000";
        wait for 50 ns;

        Y_i <= "11111111";
        X_i <= "00000000";
        wait for 50 ns;

        ----------------------------------------------------------------
        -- Y NAND X: ALUFN = 11101
        ----------------------------------------------------------------
        ALUFN_i <= "11101";

        Y_i <= "11111111";
        X_i <= "11111111";
        wait for 50 ns;

        Y_i <= "00001111";
        X_i <= "11110000";
        wait for 50 ns;

        ----------------------------------------------------------------
        -- Y XNOR X: ALUFN = 11110
        ----------------------------------------------------------------
        ALUFN_i <= "11110";

        Y_i <= "11111111";
        X_i <= "11111111";
        wait for 50 ns;

        Y_i <= "10101010";
        X_i <= "01010101";
        wait for 50 ns;

        ----------------------------------------------------------------
        -- INVALID/OTHERS
        ----------------------------------------------------------------
        ALUFN_i <= "00000";  -- Invalid, should output 0

        Y_i <= "11111111";
        X_i <= "11111111";
        wait for 50 ns;

        ALUFN_i <= "11111";  -- Invalid, should output 0

        Y_i <= "10101010";
        X_i <= "01010101";
        wait for 50 ns;

        wait;
    end process;

end architecture rtb;
